import { Service, Inject } from 'typedi';
import { Repository } from 'typeorm';
import { InjectRepository } from 'typeorm-typedi-extensions';
import { WhereInput } from 'warthog';
import { WarthogBaseService } from '../../WarthogBaseService';

import { ProposalHistory } from './proposal-history.model';

import {} from '../variants/variants.model';

import { ProposalHistoryWhereArgs, ProposalHistoryWhereInput } from '../../../generated';

@Service('ProposalHistoryService')
export class ProposalHistoryService extends WarthogBaseService<ProposalHistory> {
  constructor(@InjectRepository(ProposalHistory) protected readonly repository: Repository<ProposalHistory>) {
    super(ProposalHistory, repository);
  }

  async find<W extends WhereInput>(
    where?: any,
    orderBy?: string | string[],
    limit?: number,
    offset?: number,
    fields?: string[]
  ): Promise<ProposalHistory[]> {
    let f = fields || [];

    return this.findWithRelations<W>(where, orderBy, limit, offset, f);
  }

  async findWithRelations<W extends WhereInput>(
    _where?: any,
    orderBy?: string | string[],
    limit?: number,
    offset?: number,
    fields?: string[]
  ): Promise<ProposalHistory[]> {
    const where = <ProposalHistoryWhereInput>(_where || {});

    let mainQuery = this.buildFindQueryWithParams(<any>where, orderBy, undefined, fields, 'main').take(undefined); // remove LIMIT

    let parameters = mainQuery.getParameters();

    mainQuery = mainQuery.setParameters(parameters);

    return mainQuery
      .take(limit || 50)
      .skip(offset || 0)
      .getMany();
  }
}
